    private void parseInput(boolean isPerson, String removeItem)
    {
	Parser parser = new Parser();
	if(isPerson)
	{
	    try{
		removePerson = parser.parseStringToPerson(removeItem);
	    } catch (IOException e) {
		Logger.getLogger(this.getName()).log(Level.SEVERE, null, e);
		new SetErrorMessage("<html>"+general.getString("errFormatPerson")+" </html>");
	    }
	}
	else
	{
	    try{
		removeRoom = parser.parseStringToRoom(removeItem);
	    } catch (IOException e) {
		Logger.getLogger(this.getName()).log(Level.SEVERE, null, e);
		new SetErrorMessage("<html>"+general.getString("errFormatRoom")+" </html>");
	    }
	}
    }
    
