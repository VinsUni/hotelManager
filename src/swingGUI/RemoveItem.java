    private void removeButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_removeButtonMouseClicked
	new SwingWorker<Void, Void>() {
            @Override
            protected Void doInBackground(){
                try {
		    if(isPerson)
		    {
			HotelManagerFrame.getPersonManager().deletePerson(removePerson);
			HotelManagerFrame.getPersonListModel().removePerson(removePerson);
			HotelManagerFrame.getPersonCheckListModel().removePerson(removePerson);
			Logger.getLogger(RemoveItem.this.getName()).log(Level.INFO, "Successfuly removed person.");
		    } else{
			HotelManagerFrame.getRoomManager().deleteRoom(removeRoom);
			HotelManagerFrame.getRoomListModel().removeRoom(removeRoom);
			HotelManagerFrame.getRoomUpdateRemoveListModel().removeRoom(removeRoom);
			Logger.getLogger(RemoveItem.this.getName()).log(Level.INFO, "Successfuly removed room.");
		    }
		    
		    RemoveItem.this.setVisible(false);
		    
                    return null;
		    
                } catch (ServiceFailureException e) {
                    Logger.getLogger(RemoveItem.this.getName()).log(Level.SEVERE, null, e);
                    new SetErrorMessage("<html>"+general.getString("errDelPersonRoom")+" </html>");
                }
                return null;
            }

            @Override
            protected void done() {
                
            }
        }.execute();
    }//GEN-LAST:event_removeButtonMouseClicked

